// Tipos de funções

// Declarativas
function funcaoDeclarativa() {
    console.log("Mensagem de uma função declarativa");
}

// Expressões de funções
var funcaoDeExpressao = function() {
    console.log("Mensagem de uma Função de expressão");
}

//arrow function
var funcaoArrow = () => {
    console.log("Sou uma arrow function");
}


funcaoDeclarativa();
console.log();
funcaoDeExpressao();
console.log();
funcaoArrow();
console.log();